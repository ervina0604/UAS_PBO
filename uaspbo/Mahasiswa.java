    private void btnUpdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate1ActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("UasPboPU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();
            Mahasiswa_1 mhs = em.find(Mahasiswa_1.class, txtnim.getText());

            if (mhs != null) {
                mhs.setNama(txtnama.getText());
                mhs.setAlamat(txtalamat.getText());
                mhs.setAsalsma(txtasalsma.getText());
                mhs.setNamaorangtua(txtnamaortu.getText());

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Update Berhasil");
                tampil();
                bersih();
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                em.getTransaction().rollback();
            }
        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(null, "Update Data Gagal: " + e.getMessage());
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_btnUpdate1ActionPerformed

