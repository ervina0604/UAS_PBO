    public void tampil() {

        EntityManager em = Persistence.createEntityManagerFactory("UasPboPU").createEntityManager();

        Query q = em.createNamedQuery("Mahasiswa_1.findAll");
        List<Mahasiswa_1> hasil = q.getResultList();

        DefaultTableModel tblmk = new DefaultTableModel(new String[]{"NIM", "NAMA", "ALAMAT", "ASALSMA", "NAMAORANGTUA"}, 0);
        for (Mahasiswa_1 data : hasil) {
            Object[] baris = new Object[6];
            baris[0] = data.getNim();
            baris[1] = data.getNama();
            baris[2] = data.getAlamat();
            baris[3] = data.getAsalsma();
            baris[4] = data.getNamaorangtua();

            tblmk.addRow(baris);
        }
        table.setModel(tblmk);

    }

