    private void btnUpload1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpload1ActionPerformed
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("File yang dipilih: " + filePilihan.getAbsolutePath());

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ",";

                EntityManagerFactory emf = Persistence.createEntityManagerFactory("UasPboPU");
                EntityManager em = emf.createEntityManager();

                em.getTransaction().begin();

                while ((baris = br.readLine()) != null) {
                    String[] Mahasiswa_1 = baris.split(pemisah);

                    if (Mahasiswa_1.length == 5) {
                        String nim = Mahasiswa_1[0];
                        String nama = Mahasiswa_1[1];
                        String alamat = Mahasiswa_1[2];
                        String asalsma = Mahasiswa_1[3];
                        String namaortu = Mahasiswa_1[4];

                        if (!nim.isEmpty() && !nama.isEmpty() && !alamat.isEmpty() && !asalsma.isEmpty() && !namaortu.isEmpty()) {
                            try {
                                Mahasiswa_1 mhs = new Mahasiswa_1();
                                mhs.setNim(nim);
                                mhs.setNama(nama);
                                mhs.setAlamat(alamat);
                                mhs.setAsalsma(asalsma);
                                mhs.setNamaorangtua(namaortu);

                                em.persist(mhs);
                            } catch (Exception e) {
                                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
                            }
                        }
                    }
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diunggah");
                em.close();
                emf.close();

            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "File tidak ditemukan: " + ex.getMessage());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error Membaca File: " + ex.getMessage());
            }
        }

        tampil();
    }//GEN-LAST:event_btnUpload1ActionPerformed

